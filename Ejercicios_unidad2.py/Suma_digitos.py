'''
Leer dos números enteros de dos digitos y determinar a cuánto es igual la suma de todos los digitos
'''
s=input("Digite un número s de dos digitos: ")
q=input("Digite un número q de dos digitos: ")

m= int(s[0]) + int(s[1]) + int(q[0]) + int(q[1])

print("La suma de todos los digitos es: ", m)

